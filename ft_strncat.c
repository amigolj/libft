#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t ii;

	i = 0;
	ii = 0;
	while (s1[i])
		i++;
	while (n > ii && s2[ii])
	{
		s1[i] = s2[ii];
		i++;
		ii++;
	}
	s1[i] = '\0';
	return (s1);
}
